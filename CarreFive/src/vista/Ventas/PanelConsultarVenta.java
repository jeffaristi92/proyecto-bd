    public void setVenta(Venta v) {
        venta = controlVenta.consultarVenta(v.getidVenta());
        llenarTabla();
    }
    
    private void consultarVentaCampo() {
        String idVenta = campoBuscar.getText();

        if (idVenta.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el Nro de Venta");
        } else {
            venta = controlVenta.consultarVenta(idVenta);
            llenarTabla();
        }
    }

    private void llenarTabla() {
        campoIdVenta.setText(venta.getidVenta());
        campoCliente.setText(venta.getidCliente());
        campoEmpleado.setText(venta.getidEmpleado());
        campoFecha.setText(venta.getfechaVenta());
        DecimalFormat formato = new DecimalFormat("###,###");
        campoTotal.setText(formato.format(venta.getvlrTotal()));
        campoSaldo.setText(formato.format(venta.getsaldoVenta()));

        ArrayList<ItemVenta> datos = controlItemVenta.buscarItemVenta(venta.getidVenta());

        for (int i = 0; i < datos.size(); i++) {
            Producto p = controlProducto.consultarProducto(datos.get(i).getidProducto(),0).get(0);

            String fila[] = new String[6];
            fila[0] = datos.get(i).getidProducto();
            fila[1] = p.getdescripcion();
            fila[2] = "" + datos.get(i).getcantidad();
            fila[3] = formato.format(p.getprecioVenta());
            fila[4] = "" + datos.get(i).getiva();
            fila[5] = formato.format(datos.get(i).getvlrTotal());
            modeloItem.addRow(fila);
        }
    }

    private void consultarVentaLista() {
        PanelVentasXCliente listado = new PanelVentasXCliente(fachada, permisosEmpleado, 1);
        listado.setPanelConsulta(this);
        JFrame consulta = new JFrame();
        consulta.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        listado.setFrame(consulta);
        consulta.add(listado);
        consulta.setSize(760, 520);
        consulta.setLocationRelativeTo(null);
        consulta.setVisible(true);
        consulta.setResizable(false);
    }

