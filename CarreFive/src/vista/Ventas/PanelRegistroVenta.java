    private void bAgregarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarItemActionPerformed
        // TODO add your handling code here:
        if (p.getidProducto() == null) {
            JOptionPane.showMessageDialog(null, "Seleccione un producto");
        } else if(p.getcantDisponible()==0){
            JOptionPane.showMessageDialog(null, "El producto " +
                    p.getidProducto()+" no esta disponible");
        }else{
            iva = 0;
            int cantIngresada = 0;
            try {
                cantIngresada = Integer.parseInt(campoCantidad.getText());
            } catch (Exception exp) {
                JOptionPane.showMessageDialog(null, exp.getMessage() + ": "
                        + "Ingrese un valor valido para cantidad");
            }

            if (cantIngresada <= 0) {
                JOptionPane.showMessageDialog(null, "Ingrese una cantidad mayor que cero");
            } else if (cantIngresada > p.getcantDisponible()) {
                JOptionPane.showMessageDialog(null, "Ingrese una cantidad menor o igual"
                        + " a la cantidad disponible");
            } else {
                boolean esta = false;
                for (int i = 0; i < modeloProducto.getRowCount(); i++) {
                    esta = p.getidProducto().equals(modeloProducto.getValueAt(i, 0));
                    if (esta == true) {
                        break;
                    }
                }

                if (esta == true) {
                    JOptionPane.showMessageDialog(null, "Ya agrego el producto "
                            + p.getidProducto());
                } else {
                    String item[] = new String[6];
                    item[0] = p.getidProducto();
                    item[1] = p.getdescripcion();
                    item[2] = "" + cantIngresada;
                    DecimalFormat formato = new DecimalFormat("###,###");
                    item[3] = formato.format(p.getprecioVenta());
                    item[4] = iva + "";
                    double total = cantIngresada * p.getprecioVenta() + cantIngresada * p.getprecioVenta() * (iva / 100);
                    item[5] = formato.format(total);
                    modeloProducto.addRow(item);
                    totalVenta += total;
                    etiquetaTotal.setText(formato.format(totalVenta));
                }
            }
        }

    }//GEN-LAST:event_bAgregarItemActionPerformed

