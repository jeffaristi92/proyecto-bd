    private void botonRegistrarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarVentaActionPerformed
        // TODO add your handling code here:
        if (c.getidCliente() == null) {
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        } else if (totalVenta <= 0) {
            JOptionPane.showMessageDialog(null, "No ha agregado productos");
        } else {
            Venta v = new Venta();
            v.setidVenta(campoIdVenta.getText());
            v.setidEmpleado(permisosEmpleado.getidEmpleado());
            v.setidCliente(c.getidCliente());
            v.setfechaVenta(fecha);
            v.setvlrTotal(totalVenta);
            v.setsaldoVenta(totalVenta);
            boolean registrado = controlVenta.registrarVenta(v);
            
            if (registrado == true) {
                for (int i = 0; i < modeloProducto.getRowCount(); i++) {
                    ItemVenta item = new ItemVenta();
                    item.setnroItem("" + (i + 1));
                    item.setidVenta(numFact);
                    item.setidProducto("" + modeloProducto.getValueAt(i, 0));
                    item.setcantidad(Integer.parseInt("" + modeloProducto.getValueAt(i, 2)));
                    item.setiva(Double.parseDouble("" + modeloProducto.getValueAt(i, 4)));
                    item.setvlrTotal(Double.parseDouble("" + modeloProducto.getValueAt(i, 5)));
                    controlItemVenta.registrarItemVenta(item);
                }

                if (opcionPago.isSelected()) {
                    PagoCliente pago= new PagoCliente();
                    pago.setnroPago(1);
                    pago.setfechaPago(fecha);
                    pago.setidVenta(v.getidVenta());
                    pago.setidEmpleado(permisosEmpleado.getidEmpleado());
                    pago.setmonto(totalVenta);
                    controlPagoCliente.registrarPagoCliente(pago);
                }
                
                JOptionPane.showMessageDialog(null, "La Venta"+
                        v.getidVenta()+" se registro con exito");

            } else {
                JOptionPane.showMessageDialog(null, "No se puedo realizar la venta");
            }
        }

    }//GEN-LAST:event_botonRegistrarVentaActionPerformed

