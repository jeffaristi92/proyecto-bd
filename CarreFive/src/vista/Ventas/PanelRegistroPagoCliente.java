    private void botonRealizarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRealizarPagoActionPerformed
        // TODO add your handling code here:

        if (venta.getidVenta() == null) {
            JOptionPane.showMessageDialog(null, "Ingrese el Nro de venta");
        } else if (venta.getsaldoVenta() <= 0) {
            JOptionPane.showMessageDialog(null, "No debe nada");
        } else if (campoAbono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el monto a abonar");
        } else {
            int abono = 0;
            try {
                abono = Integer.parseInt(campoAbono.getText());
            } catch (Exception exp) {
                JOptionPane.showMessageDialog(null, exp.getMessage() + " :"
                        + "Ingrese un valor valido para el monto a abonar");
            }
            
            if (abono <= 0) {
                JOptionPane.showMessageDialog(null, "Ingrese un valor mayor a cero"
                        + " para el monto a abonar");
            } else if (abono > venta.getsaldoVenta()) {
                JOptionPane.showMessageDialog(null, "El monto a bonar debe ser"
                        + " menor o igual a el saldo");
            } else {
                int indice = controlPagoCliente.getIndicePagoCliente(venta.getidVenta());
                System.out.println("indice de pago "+venta.getidVenta()+":" +indice);
                PagoCliente pc = new PagoCliente();
                pc.setnroPago(indice + 1);
                pc.setidVenta(venta.getidVenta());
                pc.setfechaPago(campoFechaPago.getDate().toString());
                pc.setmonto(abono);
                pc.setidEmpleado(permisosEmpleado.getidEmpleado());
                boolean registrado =controlPagoCliente.registrarPagoCliente(pc);
                if(registrado){
                    JOptionPane.showMessageDialog(null, "Pago registrado con exito");
                    buscarVenta(venta.getidVenta());
                }else{
                    JOptionPane.showMessageDialog(null, "No se pudo registrar el pago");
                }
                
            }
        }
    }//GEN-LAST:event_botonRealizarPagoActionPerformed

