    public void buscarVenta(String idVenta) {
        campoIdVenta.setText(idVenta);
        venta = controlVenta.consultarVenta(idVenta);
        Cliente c = controlCliente.consultarCliente(venta.getidCliente(), 0).get(0);
        campoCliente.setText(c.getnombreCliente() + " " + c.getapellidoCliente());
        campoFechaVenta.setText(venta.getfechaVenta());
        DecimalFormat formato = new DecimalFormat("###,###");
        campoSaldo.setText(formato.format(venta.getsaldoVenta()));
    }
    
    void setVenta(Venta v) {
        buscarVenta(v.getidVenta());
    }
    
    void buscarLista(){
        PanelVentasXCliente listado = new PanelVentasXCliente(fachada, permisosEmpleado, 2);
        listado.setPanelRegistroPagoCliente(this);
        JFrame consulta = new JFrame();
        consulta.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        listado.setFrame(consulta);
        consulta.add(listado);
        consulta.setSize(760, 520);
        consulta.setLocationRelativeTo(null);
        consulta.setVisible(true);
        consulta.setResizable(false);
    }
    
    void buscarCodigo(){
        String idVenta = campoIdVenta.getText();
        if (idVenta.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el numero de Venta");
        } else {
            buscarVenta(idVenta);
        }
    }
    
