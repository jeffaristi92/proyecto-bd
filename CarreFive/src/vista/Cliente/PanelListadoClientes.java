    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeleccionarActionPerformed
        // TODO add your handling code here:
        int fila = tablaClientes.getSelectedRow();
        
        if(fila !=-1){
            Cliente c = new Cliente();
            c.setidCliente(tablaClientes.getValueAt(fila,0)+"");
            c.setnombreCliente(tablaClientes.getValueAt(fila,1)+"");
            c.setapellidoCliente(tablaClientes.getValueAt(fila,2)+"");
            switch(opcion){
                case 1:
                    ventasNuevas.setCliente(c);
                    break;
                case 2:
                    ventasAntiguas.setCliente(c);
                    break;
                case 3:
                    ventasXcliente.setCliente(c);
                    break;
            }
            
            frame.dispose();

        }
    }//GEN-LAST:event_botonSeleccionarActionPerformed

