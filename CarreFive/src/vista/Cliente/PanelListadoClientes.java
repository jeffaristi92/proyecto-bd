    private void initSecundario(FachadaBD f,Empleado pE){
        permisosEmpleado = pE;
        fachada=f;
        controlCliente = new controladorCliente(fachada);
        modeloClientes = new DefaultTableModel();
        modeloClientes.addColumn("Cedula");
        
        modeloClientes.addColumn("Nombre");
        modeloClientes.addColumn("Apellido");
        modeloClientes.addColumn("Direccion");
        modeloClientes.addColumn("Telefono");
        modeloClientes.addColumn("Saldo");
        
        Object[][] datos = controlCliente.listarClientes();
        for(int i=0;i<datos.length;i++){
            modeloClientes.addRow(datos[i]);
        }
    }
    
