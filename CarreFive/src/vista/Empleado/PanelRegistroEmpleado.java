    private void botonInsertarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarEmpleadoActionPerformed
        // TODO add your handling code here:
        String idEmpleado = campoId.getText();
        String nombre = campoNombre.getText();
        String apellido = campoApellido.getText();
        String direccion = campoDireccion.getText();
        String telefono = campoTelefono.getText();
        String cargo = campoCargo.getText();
        
        if (idEmpleado.equals("") || nombre.equals("") || apellido.equals("") || cargo.equals("")) {
            JOptionPane.showMessageDialog(null,"Llene todos los campos obligatorios");
        } else {
            Empleado e = new Empleado();
            e.setidEmpleado(idEmpleado);
            e.setnombreEmpleado(nombre);
            e.setapellidoEmpleado(apellido);
            e.setdireccionEmpleado(direccion);
            e.settelefonoEmpleado(telefono);
            e.setcargoEmpleado(cargo);
            
            boolean registrado =controlador.registrarEmpleado(e);
            
            if(registrado==true){
                JOptionPane.showMessageDialog(null,"El empleado se ha registrado con exito");
            }else{
                JOptionPane.showMessageDialog(null,"No se pudo registrar el empleado");
            }
        }
    }//GEN-LAST:event_botonInsertarEmpleadoActionPerformed
